class Student {
    constructor(id, name, email, mobile, schoolName, standard, division, state, country) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.mobile = mobile;
        this.schoolName = schoolName;
        this.standard = standard;
        this.division = division;
        this.state = state;
        this.country = country;
    }
}

export default Student;